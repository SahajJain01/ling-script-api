app.post('/lang/create', async (req, res) => {
  const { code } = req.body
  const result = await prisma.lang.create({
    data: {
      code
    },
  })
  res.json(result)
})


app.post('/prompt/create', async (req, res) => {
  const { content, answer, langId } = req.body
  const result = await prisma.prompt.create({
    data: {
      content,
      answer,
      langId,
    },
  })
  res.json(result)
})

